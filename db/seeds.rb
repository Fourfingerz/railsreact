p = Post.new(title: 'idolatry of uniqueness that afflicts college students and addicts', author: 'David Foster Wallace')
p.contents = <<EOF
People of a certain age and level of like life-experience believe they're immortal: 
college students and alcoholics/addicts are the worst: they deep-down believe they're 
exempt from the laws of physics and statistics that ironly govern everybody else. 
They'll piss and moan your ear off if somebody else fucks with the rules, but they 
don't deep down see themselves subject to them, the same rules. And they're 
constitutionally unable to learn from anybody else's experience: if some jaywalking 
B.U. student does get his car towed, your other student's or addict's response to this 
will be to ponder just what imponderable difference makes it possible for that other 
guy to get splattered or towed and not him, the ponderer. They never doubt the 
difference — they just ponder it. It's like a kind of idolatry of uniqueness.
EOF

