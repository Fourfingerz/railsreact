p = Post.new(title: 'I am Complex', author: 'Tennis Boy')
p.contents = <<EOF
'I read,' I say. 'I study and read. I bet I've read everything you've read. 
Don't think I haven't. I consume libraries. I wear out spines and ROM-drives. 
I do things like get in a taxi and say, "The library, and step on it." 
My instincts concerning syntax and mechanics are better than your own, 
I can tell, with due respect. But it transcends the mechanics. I'm not a machine. 
I feel and believe. I have opinions. Some of them are interesting. 
I could, if you'd let me, talk and talk. Let's talk about anything. 
I believe the influence of Kierkegaard on Camus is underestimated. 
I believe Dennis Gabor may very well have been the Antichrist. 
I believe Hobbes is just Rousseau in a dark mirror. 
I believe, with Hegel, that transcendence is absorption. 
I could interface you guys right under the table,' 
I say. 'I'm not just a creatus, manufactured, conditioned, bred for a function.'
EOF

